package adaptlil.data_classes.geometry;

public class Cartesian2D {
    protected float x;
    protected float y;

    public Cartesian2D(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
