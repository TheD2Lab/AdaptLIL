package geometry;

import com.fasterxml.jackson.annotation.JsonProperty;

public class Shape {

    @JsonProperty("x")
    public float x;
    @JsonProperty("y")
    public float y;
    @JsonProperty("width")
    public float width;
    @JsonProperty("height")
    public float height;

    public Shape(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
    public Shape() {

    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public void setWidth(float width) {
        this.width = width;
    }

    public void setHeight(float height) {
        this.height = height;
    }
}
